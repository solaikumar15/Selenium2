package Day8;

public class Calculator {
	
	public void add(int a, int b) {
		System.out.println(a + b);
	}

	public void subtract(int a, int b) {
		System.out.println(a - b);
	}

	public int multiply(int a, int b) {
		return (a * b);
	}

	public void divide(int a, int b) {
		System.out.println(a / b);
	}

}
